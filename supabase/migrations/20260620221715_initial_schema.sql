-- 6. Notification Log Table
CREATE TABLE notification_log (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    week_date TEXT NOT NULL,
    sent_at TIMESTAMPTZ DEFAULT NOW(),
    rider_count INTEGER
);